    const [isAuthor, setIsAuthor] = useState(false);
    const [isPublished, setisPublished] = useState(false);
    const [isLoading, setIsLoading] = useState(true);
    const [error, setError] = useState(null);
    const navigate = useNavigate();

    useEffect(() => {
        PostService.getSinglePost(Number(id))
            .then((data) => {
                setIsLoading(false);
                setData(data.data);
                setisPublished(data.data.published);
                setError(null);
                if (data.data.user_id !== user.id && !user.is_admin) {
                    navigate('/');
                } else if (data.data.user_id === user.id) {
                    setIsAuthor(true);
                }
            }).catch(err => {
                setIsLoading(false);
                setError(err.message);
            })
    }, []);

    const handleDelete = () => {
        if (isAuthor) {
            PostService.deletePost(data!.id)
                .then(() => {
                    setIsLoading(false);
                    setError(null);
                    navigate('/posts')
                }).catch(err => {
                    setIsLoading(false);
                    setError(err.message);
                })
        }
    }

    const handleApprove = () => {
        if (user.is_admin && !isPublished) {
            PostService.approvePost(Number(id))
                .then(()=>{
                    setisPublished(true);
                })
                .catch(err => {
                    setIsLoading(false);
                    setError(err.message);
                })
        }
    }

